package deiteiPJCourse.Lab5;

public class lab5 {
    public static void main(String[] args) {

        int start = -5;
        int count = 1;
        int step = 2 ;

        for(int n = start; n < count; n+=step){
           switch (n){
               case 0:
                   System.out.println("Hello world");
                   break;
               case 1:
                   System.out.println("How are you doing");
                   break;
               case 2:
                   System.out.println("Well done");
                   break;
               case 3:
                   System.out.println("Sunny day");
                   break;
               case 4:
                   System.out.println("Fuck the police");
                   break;
               case 5:
                   System.out.println("Marh Mellou");
                   break;
            }
        }
    }
}
