package deiteiPJCourse.Lab5;

public class Switcher {
    public static void main(String[] args) {

        int n = 1;
        switch (n) {
            case 5:
                System.out.println("n: " + n);
                break;
            case 10:
                System.out.println("n: " + n);
                break;
            default:
                System.out.println("Well done");
                break;
        }
    }
}

