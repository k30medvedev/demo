package CompareToExample;

public class Montain {

    private String name;
    private Integer hight;

    public Montain(String name, int hight) {
        this.name = name;
        this.hight = hight;
    }


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getHight() {
        return hight;
    }

}
